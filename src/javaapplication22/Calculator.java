public class Calculator {
    
    double Add(double x, double y) {
        return x + y;
    }
    
    double Sub(double x, double y) {
        return x - y;
    }
    
    double Mult(double x, double y) {
        return x * y;
    }
    
    double Div(double x, double y) {
        return x / y;
    }
}
